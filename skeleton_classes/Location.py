class Location:
    def __init__(self, location_name, longitude, latitude, is_active, is_study_location, is_parking_lot):
        self.location_name = location_name
        self.longitude = longitude
        self.latitude = latitude
        self.is_active = is_active
        self.is_study_location = is_study_location
        self.is_parking_lot = is_parking_lot
